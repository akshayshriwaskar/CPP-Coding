//C++ program to illustrate use of input and output operators.
#include<iostream>
using namespace std;
int main()
{

    int num1,num2;
    cout<<"\nEnter two numbers:";
    cin>>num1>>num2; //cascaded input operators
    cout<<"\n Two numbers are:"<<num1<<","<<num2; //cascaded input operators
    return 0;
}
